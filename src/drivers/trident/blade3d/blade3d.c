static int init9880(void)
{
	unprotect_reg();

	//if use SDRAM
	//outp(Port_CRX, 0x2a);
	//outp(Port_CRX+1, (inp(Port_CRX+1) & ~0x0c) | 0x08);
	//disable other card

	//enable VGA
	outp(0x3c3, 0x01);
	outp(0x4e68, 0x08);

	outp(0x3c2, 0x23);

	//if chip revB
	if (CHIPREV == 0x02) {
		printk(BIOS_DEBUG, "blade3d: chip revision B\n");
		outp(Port_CRX, 0xc0);
		outp(Port_CRX + 1, 0x00);
	}
	config_OEM_regs();
	init_SGRAM();
	//config_video_memory();
	set_video_mode();
	reprotect_reg();
	//screen on
	outp(Port_SRX, 0x01);
	outp(Port_SRX + 1, inp(Port_SRX + 1) & ~0x20);

	return 0;
}

