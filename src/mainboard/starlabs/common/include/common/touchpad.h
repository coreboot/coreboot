#ifndef _STARLABS_CMN_TOUCHPAD_H_
#define _STARLABS_CMN_TOUCHPAD_H_

#include <stdint.h>

#define STARLABS_TOUCHPAD_HAPTICS_DEFAULT		2
#define STARLABS_TOUCHPAD_HAPTICS_MAX			4

#define STARLABS_TOUCHPAD_FORCE_MINIMAL		50
#define STARLABS_TOUCHPAD_FORCE_LOW		125
#define STARLABS_TOUCHPAD_FORCE_AVERAGE		250
#define STARLABS_TOUCHPAD_FORCE_HIGH		350
#define STARLABS_TOUCHPAD_FORCE_HULK		500

#define STARLABS_TOUCHPAD_PRESS_FORCE_DEFAULT		STARLABS_TOUCHPAD_FORCE_AVERAGE
#define STARLABS_TOUCHPAD_RELEASE_FORCE_DEFAULT		STARLABS_TOUCHPAD_FORCE_MINIMAL

#define STARLABS_TOUCHPAD_RATE_RELAXED		80
#define STARLABS_TOUCHPAD_RATE_BALANCED		111
#define STARLABS_TOUCHPAD_RATE_FAST		160
#define STARLABS_TOUCHPAD_RATE_LUDICROUS	208
#define STARLABS_TOUCHPAD_RATE_PLAID		255

#define STARLABS_TOUCHPAD_REPORT_RATE_DEFAULT		111
#define STARLABS_TOUCHPAD_REPORT_RATE_MIN		1
#define STARLABS_TOUCHPAD_REPORT_RATE_MAX		255

#define STARLABS_TOUCHPAD_HID_DESC_REG		0x0020
#define STARLABS_TOUCHPAD_I2C_ADDR		0x2c
#define STARLABS_TOUCHPAD_I2C_BUS		0

#define STARLABS_TOUCHPAD_HAPTICS_REPORT_ID	0x0a
#define STARLABS_TOUCHPAD_FORCE_REPORT_ID	0x0b
#define STARLABS_TOUCHPAD_USER_REG_REPORT_ID	0x43
#define STARLABS_TOUCHPAD_USER_REG_BANK		0x00
#define STARLABS_TOUCHPAD_USER_REG_ADDR_RATE	0x12

/*
 * The HID descriptor may not be live immediately when this hook runs, but
 * these command/data registers are stable enough to perform early HID bring-up.
 */
#define STARLABS_TOUCHPAD_FALLBACK_CMD_REG	0x0022
#define STARLABS_TOUCHPAD_FALLBACK_DATA_REG	0x0023

#define STARLABS_TOUCHPAD_RETRIES		3
#define STARLABS_TOUCHPAD_RETRY_DELAY_MS	20
#define STARLABS_TOUCHPAD_WAKE_DELAY_MS		1
#define STARLABS_TOUCHPAD_VERIFY_RETRIES	8
#define STARLABS_TOUCHPAD_VERIFY_DELAY_MS	5
#define STARLABS_TOUCHPAD_RESET_READY_RETRIES	24
#define STARLABS_TOUCHPAD_RESET_READY_DELAY_MS	5

#define I2C_HID_OPCODE_RESET			0x01
#define I2C_HID_OPCODE_GET_REPORT		0x02
#define I2C_HID_OPCODE_SET_REPORT		0x03
#define I2C_HID_OPCODE_SET_POWER		0x08
#define I2C_HID_REPORT_TYPE_FEATURE		0x03

#define I2C_HID_PWR_ON				0x00

enum starlabs_touchpad_desc_offset {
	TOUCHPAD_DESC_CMD_REG		= 16,
	TOUCHPAD_DESC_DATA_REG		= 18,
	TOUCHPAD_DESC_LENGTH		= 30,
};

struct starlabs_touchpad_op_ctx {
	unsigned int bus;
	uint16_t cmd_reg;
	uint16_t data_reg;
	uint16_t press;
	uint16_t release;
	uint8_t bank;
	uint8_t addr;
	uint8_t value;
	uint8_t level;
};

#endif /* _STARLABS_CMN_TOUCHPAD_H_ */
