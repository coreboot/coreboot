#define DEFAULT_RCBA	0xfed1c000

static void init_egress(void)
{
	/* VC0: TC0 only */
	EPBAR8(0x14) = 1;
	EPBAR8(0x4) = 1;

	/* VC1: ID1, TC7 */
	EPBAR32(0x20) = (EPBAR32(0x20) & ~(7 << 24)) | (1 << 24);
	EPBAR8(0x20) = 1 << 7;

	/* VC1: enable */
	EPBAR32(0x20) |= 1 << 31;

	while ((EPBAR8(0x26) & 2) != 0) ;

	printk(BIOS_DEBUG, "Done EP loop\n");
}

