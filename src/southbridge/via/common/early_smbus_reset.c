#include "via_early_smbus.h"

/**
 * \brief Clear the SMBus host status register
 *
 * @param smbus_dev The base SMBus IO port
 */
void smbus_reset(u32 smbus_dev)
{
	outb(0xdf, SMBHSTSTAT(smbus_dev));
}
